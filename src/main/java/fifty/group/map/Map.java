    public void drawTileLayer(Graphics2D g2d) {
        int x = 0;
        int y = 0;

        for (int i = 0; i < integerMap.length-1; i++) {
            for (int j = 0; j < integerMap[i].length-1; j++) {
                g2d.drawImage(spriteMap.get(getTileNeighbours(i, j)), x, y, null);
                x += 32;
            }
            y += 32;
            x = 0;
        }
    }

